        '1.2^20110303'   | '1.2.0.20110303'
        '1.2.3^20110303' | '1.2.3.20110303'
        '1/20110303'     | '1.0.0.20110303'
        '1.2/20110303'   | '1.2.0.20110303'
        '1.2.3/20110303' | '1.2.3.20110303'
        '1-20.11$03@0#3' | '1.0.0.20_11_03_0_3'
        '100000'         | '100000'
    }
}
